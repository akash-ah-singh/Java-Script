// Primitive type

let num1=6;
let num2=num1;
console.log(num1);
console.log(num2);
num1++;
console.log(num1);
console.log(num2);


// Reference type

let arr1=[1,2,3,4,5];
let arr2=arr1;
console.log(arr1);
console.log(arr2);
arr1.push(10);
console.log(arr1);
console.log(arr2);




