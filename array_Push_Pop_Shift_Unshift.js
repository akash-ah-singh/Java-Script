// push , pop 


// 1. Push 


let fruits1=['mango' ,'banana', 'apple' ,'orange'];

fruits1.push('pineapple');
console.log(fruits1);


// 2. Pop 


let fruits2=['mango' ,'banana', 'apple' ,'orange'];

popedFruits=fruits2.pop();
console.log(popedFruits);
console.log(fruits2);



// unshift , shift


// 1. unshift 


let fruits3=['mango' ,'banana', 'apple' ,'orange'];

fruits3.unshift('pineapple');
console.log(fruits3);


// 2. shift 

let fruits4=['mango' ,'banana', 'apple' ,'orange'];

shiftedFruits=fruits4.shift();
console.log(shiftedFruits);
console.log(fruits4);



