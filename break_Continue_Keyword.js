// break keyword 


console.log("Use fo break keyword : ");


for(let i=1;i<=10;i++){
    if(i===6){
        break;
    }
    console.log(i);
}



// continue keyword 

console.log("Use fo continue keyword : ");

for(let i=1;i<=10;i++){
    if(i===6){
        continue;
    }
    console.log(i);
}