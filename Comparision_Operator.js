// Comparision Operator 


let num1=5;
let num2=8;

// 1.>
console.log(num1>num2);
// 2.>=
console.log(num1>=num2);
// 3.<
console.log(num1<num2);
// 4.<=
console.log(num1<=num2);


// 5. == VS ===   (double equal VS triple equal)

let newNum1=7;
let newNum2=7;

console.log(newNum1==newNum2);




let newNum3="7";
let newNum4=7;

console.log(newNum3==newNum4);
console.log(newNum3===newNum4);






// 6. != VS !==  (not equal  VS  not double equal )



let newNum5=7;
let newNum6=7;

console.log(newNum5!=newNum6);



let newNum7="7";
let newNum8=7;

console.log(newNum7!=newNum8);
console.log(newNum7!==newNum8);


